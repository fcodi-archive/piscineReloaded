int		ft_sqrt(int nb)
{
	int		i;
	int		result;

	if (nb < 1)
		return (0);
	result = 0;
	i = 0;
	while (++i <= nb)
	{
		result = nb / i;
		if (nb % i == 0 && result == i)
			return (result);
	}
	return (0);
}
